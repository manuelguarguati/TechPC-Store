// VERIFICAR CÃ“DIGO OTP (POST /auth/verificar)
// --------------------------------------------------------------
router.post('/verificar', async (req, res) => {
  try {
    const { codigo } = req.body;

    const temp = req.session.tempUser || req.session.tempGoogleUser;
    if (!temp) return res.status(400).json({ success: false, message: 'SesiÃ³n expirada' });

    if (parseInt(codigo) !== temp.codigo)
      return res.status(400).json({ success: false, message: 'CÃ³digo incorrecto' });

    const hashedPassword = await bcrypt.hash(temp.password, 10);

    let user = await User.findOne({ where: { email: temp.email } });

    if (user) {
      // Actualizar usuario existente
      await user.update({
        phone: temp.phone,
        password: hashedPassword,
        phone_verified: true
      });
    } else {
      // Crear nuevo usuario
      user = await User.create({
        name: temp.name,
        lastname: temp.lastname || '',
        email: temp.email,
        phone: temp.phone,
        password: hashedPassword,
        phone_verified: true,
        role: 'user'
      });
    }

    // Guardar sesiÃ³n completa
    req.session.user = {
      id: user.id,
      name: user.name,
      lastname: user.lastname || '',
      email: user.email,
      phone: user.phone || '',
      role: user.role || 'user'
    };

    // Limpiar sesiones temporales
    delete req.session.tempUser;
    delete req.session.tempGoogleUser;

    res.json({ success: true, message: 'Cuenta verificada y completada', redirect: '/home' });

  } catch (err) {
    console.error('Error en /auth/verificar:', err);
    res.status(500).json({ success: false, message: 'Error interno al verificar cÃ³digo' });
  }
});

