router.get('/completar-registro', (req, res) => {
  if (!req.session.tempGoogleUser && !req.session.tempUser) return res.redirect('/login');

  const temp = req.session.tempGoogleUser || req.session.tempUser;
  const { name, lastname, email } = temp;

  res.render('completar-registro', { name, lastname, email });
});

// --------------------------------------------------------------
// LOGIN CON GOOGLE (POST /auth/google-login)
// --------------------------------------------------------------
router.post('/google-login', async (req, res) => {
  try {
    const { id_token } = req.body;

    const ticket = await client.verifyIdToken({
      idToken: id_token,
      audience: process.env.GOOGLE_CLIENT_ID
    });

    const payload = ticket.getPayload();
    const googleEmail = payload.email;
    const googleName = payload.name || 'Usuario Google';

    let user = await User.findOne({ where: { email: googleEmail } });

    if (!user) {
      req.session.tempGoogleUser = { name: googleName, lastname: '', email: googleEmail };
      await req.session.save();
      return res.json({
        success: true,
        message: 'Completa tu registro agregando telÃ©fono y contraseÃ±a',
        redirect: '/completar-registro'
      });
    }

    if (!user.phone || !user.password) {
      req.session.tempGoogleUser = {
        id: user.id,
        name: user.name,
        lastname: user.lastname || '',
        email: user.email
      };
      await req.session.save();
      return res.json({
        success: true,
        message: 'Debes completar tu perfil con telÃ©fono y contraseÃ±a',
        redirect: '/completar-registro'
      });
    }

    req.session.user = {
      id: user.id,
      name: user.name,
      lastname: user.lastname || '',
      email: user.email,
      phone: user.phone || '',
      role: user.role || 'user'
    };

    // âœ… Enviar rol al frontend
    res.json({
      success: true,
      message: 'Inicio de sesiÃ³n con Google exitoso',
      redirect: user.role === 'admin' ? '/admin' : '/home',
      role: user.role
    });

  } catch (err) {
    console.error('Error en /auth/google-login:', err);
    res.status(500).json({ error: 'Error al iniciar sesiÃ³n con Google' });
  }
});


// --------------------------------------------------------------
// LOGIN NORMAL (POST /auth/login)
// --------------------------------------------------------------
router.post('/login', async (req, res) => {
  try {
    const { email, password } = req.body;

    const user = await User.findOne({ where: { email } });
    if (!user) return res.status(400).json({ error: 'Usuario no encontrado' });

    const valid = await bcrypt.compare(password, user.password);
    if (!valid) return res.status(400).json({ error: 'ContraseÃ±a incorrecta' });

    req.session.user = {
      id: user.id,
      name: user.name,
      lastname: user.lastname || '',
      email: user.email,
      phone: user.phone || '',
      role: user.role || 'user'
    };

    // âœ… Enviar rol al frontend
    res.json({
      success: true,
      message: 'Inicio de sesiÃ³n exitoso',
      redirect: user.role === 'admin' ? '/admin' : '/home',
      role: user.role
    });

  } catch (err) {
    console.error('Error en /auth/login:', err);
    res.status(500).json({ error: 'Error interno del servidor' });
  }
});


