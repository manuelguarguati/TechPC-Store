const express = require('express');
const bcrypt = require('bcryptjs');
const { OAuth2Client } = require('google-auth-library');
const User = require('../models/User'); // Modelo Sequelize
const twilio = require('twilio');
const authController = require('../controllers/authController');
const router = express.Router();

// ðŸŸ¢ ConfiguraciÃ³n Twilio
const twilioClient = twilio(process.env.TWILIO_ACCOUNT_SID, process.env.TWILIO_AUTH_TOKEN);
const FROM = process.env.TWILIO_FROM_NUMBER;

// ðŸŸ¢ Cliente OAuth de Google
const client = new OAuth2Client(process.env.GOOGLE_CLIENT_ID);

// --------------------------------------------------------------
// FUNCION AUXILIAR: normalizar telÃ©fono a formato E.164 (+57xxxx)
// --------------------------------------------------------------
function formatPhone(phone) {
  let clean = phone.replace(/\D/g, ''); // eliminar todo lo que no sea nÃºmero
  if (clean.length === 10) return '+57' + clean; // agregar +57 si tiene 10 dÃ­gitos
  if (clean.length === 12 && clean.startsWith('57')) return '+' + clean; // si ya tiene 57
  if (clean.startsWith('+') && clean.length >= 12) return clean; // ya con cÃ³digo internacional
  return null; // formato invÃ¡lido
}

